"""
=============================================================================
Dataset Saver -- reference/dataset_used/save_dataset.py
=============================================================================
Generates the training dataset used by Block II/III and saves it as:
  - block2_dataset.npz   : full dataset (O_images, X_images, labels, metadata)
  - O_canonical.npy       : canonical O image (8x8)
  - X_canonical.npy       : canonical X image (8x8)
  - dataset_summary.txt   : human-readable summary

Re-running this script with the same seed reproduces the exact same dataset.
=============================================================================
"""

import sys
from pathlib import Path
import numpy as np

_HERE = Path(__file__).resolve().parent
_ROOT = _HERE.parent.parent

# Add project root so we can import data_generator
sys.path.insert(0, str(_ROOT))
from data_generator import generate_dataset, O_CANONICAL, X_CANONICAL


def save_dataset(out_dir: Path, n_per_class: int = 50, seed: int = 42,
                 tag: str = "block2"):
    """
    Generate and save a dataset to disk.

    Args:
        out_dir     : directory to save into
        n_per_class : number of images per class
        seed        : random seed (reproducibility)
        tag         : prefix for filenames (e.g. 'block2', 'block4_trial03')
    Returns:
        Path to the saved .npz file
    """
    out_dir = Path(out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    # Generate
    ds = generate_dataset(n_per_class=n_per_class, seed=seed)

    O_images = np.stack(ds['O_images'])       # (n_per_class, 8, 8)
    X_images = np.stack(ds['X_images'])       # (n_per_class, 8, 8)
    labels   = np.array(ds['labels'])         # (2*n_per_class,)

    # Save .npz archive
    npz_path = out_dir / f"{tag}_dataset.npz"
    np.savez_compressed(
        npz_path,
        O_images=O_images,
        X_images=X_images,
        labels=labels,
        n_per_class=np.array(n_per_class),
        seed=np.array(seed),
    )
    print(f"[SAVED] {npz_path}  ({npz_path.stat().st_size:,} bytes)")

    # Save canonical images separately (easy visual inspection)
    np.save(out_dir / "O_canonical.npy", O_CANONICAL)
    np.save(out_dir / "X_canonical.npy", X_CANONICAL)

    # Save human-readable summary
    summary_path = out_dir / f"{tag}_dataset_summary.txt"
    with open(summary_path, "w") as f:
        f.write(f"Dataset: {tag}\n")
        f.write(f"Generated by: save_dataset.py\n")
        f.write(f"Seed: {seed}\n")
        f.write(f"N per class: {n_per_class}\n")
        f.write(f"Total images: {2 * n_per_class}\n")
        f.write(f"Image shape: 8x8 binary\n")
        f.write(f"\n--- O images ---\n")
        f.write(f"Count: {len(ds['O_images'])}\n")
        o_pixels = [img.sum() for img in ds['O_images']]
        f.write(f"Pixel counts: min={min(o_pixels):.0f}, "
                f"max={max(o_pixels):.0f}, "
                f"mean={np.mean(o_pixels):.1f}\n")
        f.write(f"\n--- X images ---\n")
        f.write(f"Count: {len(ds['X_images'])}\n")
        x_pixels = [img.sum() for img in ds['X_images']]
        f.write(f"Pixel counts: min={min(x_pixels):.0f}, "
                f"max={max(x_pixels):.0f}, "
                f"mean={np.mean(x_pixels):.1f}\n")
        f.write(f"\n--- Canonical O (index 0) ---\n")
        for r in range(8):
            f.write(" ".join(f"{int(O_CANONICAL[r,c])}" for c in range(8)) + "\n")
        f.write(f"\n--- Canonical X (index 0) ---\n")
        for r in range(8):
            f.write(" ".join(f"{int(X_CANONICAL[r,c])}" for c in range(8)) + "\n")

    print(f"[SAVED] {summary_path}")

    # Save visual grid (if matplotlib available)
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt

        fig, axes = plt.subplots(4, 10, figsize=(15, 6))
        fig.suptitle(f"Dataset: {tag}  (seed={seed}, n={n_per_class}/class)",
                     fontsize=12, fontweight='bold')
        # Top 2 rows: O images (first 20)
        for i in range(20):
            r, c = divmod(i, 10)
            ax = axes[r][c]
            idx = min(i, len(ds['O_images']) - 1)
            ax.imshow(ds['O_images'][idx], cmap='Greys', vmin=0, vmax=1)
            ax.set_xticks([]); ax.set_yticks([])
            if i == 0:
                ax.set_title("O*", fontsize=8, color='blue')
        # Bottom 2 rows: X images (first 20)
        for i in range(20):
            r, c = divmod(i, 10)
            ax = axes[r + 2][c]
            idx = min(i, len(ds['X_images']) - 1)
            ax.imshow(ds['X_images'][idx], cmap='Greys', vmin=0, vmax=1)
            ax.set_xticks([]); ax.set_yticks([])
            if i == 0:
                ax.set_title("X*", fontsize=8, color='red')

        fig_path = out_dir / f"{tag}_dataset_grid.png"
        fig.tight_layout()
        fig.savefig(fig_path, dpi=150)
        plt.close(fig)
        print(f"[SAVED] {fig_path}")
    except ImportError:
        print("[SKIP] matplotlib not available -- grid figure skipped")

    return npz_path


# =========================================================================
# Main: save the Block II training dataset
# =========================================================================
if __name__ == "__main__":
    print("=" * 50)
    print("Saving Block II/III training dataset")
    print("=" * 50)

    save_dataset(
        out_dir=_HERE,
        n_per_class=50,
        seed=42,
        tag="block2",
    )

    print("\nDone.")
